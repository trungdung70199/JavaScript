// Math Methods
// Syntax: Math.method(number)

// Math.round(): returns the nearest integer
console.log("Math.round method's example: ");
console.log(Math.round(4.6));
console.log(Math.round(4.5));
console.log(Math.round(5.9));

// Math.ceil(): returns the value of i rounded UP to its nearest integer
console.log("Math.ceil method's example: ");
console.log(Math.ceil(4.8));
console.log(Math.ceil(4.5));
console.log(Math.ceil(4.1));
console.log(Math.ceil(-4.3));

// Math.floor(): returns the value of x rounded down to its nearest integer
console.log("Math.floor method'example: ");
console.log(Math.floor(2.4));
console.log(Math.floor(2.7));
console.log(Math.floor(-2.2));
console.log(Math.floor(2.9));

// Math.sign(): returns if x is negative null or positive
console.log("Math.sign method's example: ");
console.log(Math.sign(-2));
console.log(Math.sign(3));
console.log(Math.sign(0));
console.log(Math.sign(1));