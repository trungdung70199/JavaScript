// JavaScript Arrays
// Arrays is a special variable which can hold more than one value
const cars = ["GTR", "Volvo", "BMW"];
console.log(cars)

// An array can hold many values under a single name,
// can access the values by referring to an index
// Using keyword new
const car = new Array("Ford", "Toyota", "Ferrari");
console.log(car);
// Accessing Array:
console.log(car[0]);
// Changing an Array
car[1] = "Opel";
console.log(car);